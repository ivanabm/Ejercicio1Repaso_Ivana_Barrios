package ejercicios;

public class Ejercicio30 {

    public static void main(String[] args) {

            int a = 64;
            int raiz = (int) Math.sqrt(a);

            if (raiz * raiz == a) {
                System.out.println(a + " es un cuadrado perfecto.");
            } else {
                System.out.println(a + " no es un cuadrado perfecto.");
            }
    }
}



